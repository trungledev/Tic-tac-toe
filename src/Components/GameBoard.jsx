import { useState } from "react";
export default function GameBoard({ gameBoard, onChangeBoard }) {
  function handleClick(rowIndex, columnIndex) {
    onChangeBoard(rowIndex, columnIndex);
  }
  return (
    <ol id="game-board">
      {gameBoard.map((row, rowIndex) => (
        <li key={rowIndex}>
          <ol>
            {row.map((symbol, colIndex) => (
              <li key={colIndex}>
                <button onClick={() => handleClick(rowIndex, colIndex)}>
                  {symbol}
                </button>
              </li>
            ))}
          </ol>
        </li>
      ))}
    </ol>
  );
}
